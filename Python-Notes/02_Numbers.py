"""
Programming With Numbers

This file shows basic arithmetic operations in Python.
"""

# Adding two numbers
Add = 10 + 2
print('Adding Two Numbers:', Add)

print('---------------')

# Subtracting two numbers
Subtract = 10 - 2
print('Subtracting Two Numbers:', Subtract)

print('---------------')

# Multiplying two numbers
Multiply = 10 * 2
print('Multiplying Two Numbers:', Multiply)

print('---------------')

# Dividing two numbers
Divide = 10 / 2
print('Dividing Two Numbers:', Divide)

print('---------------')

# Finding the modulus of two numbers
Modulus = 10 % 2
print('Modulus of Two Numbers:', Modulus)

print('---------------')

# Finding the exponent of two numbers
Exponent = 10 ** 2
print('Exponent of Two Numbers:', Exponent)

print('---------------')
print('---------------')

# Example: Finding the speed of a car
