"""
Using Modules in Python

Modules let us reuse code written by other developers.
"""

# Importing the math module to use built-in mathematical functions
import math

# Using the square root function from the math module

print("Finding Sqrt of 16",math.sqrt(16))

print("Printing 2's Power 4 ",math.pow(2,4))

print(math.pi)

print(math.log10(100))

print(math.log10(1000))

print(math.floor(2.3))

print(math.ceil(2.3))











